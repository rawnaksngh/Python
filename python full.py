# 1.	Write a Program to interchange first and last elements in a list

def swap_list(new_list):
  size = len(new_list)

  # Swapping
  temp = new_list[0]
  new_list[0] = new_list[size - 1]
  new_list[size - 1] = temp

  return new_list

newList = [12, 35, 9, 56, 24]

print(swap_list(newList))


# 30.	Program to print duplicates from a list of integers

def print_duplicates(input_list):
  seen = set()
  duplicates = set()
  for num in input_list:
    if num in seen:
      duplicates.add(num)
    else:
      seen.add(num)
  print("Duplicate elements in the list:", list(duplicates))

my_list = [1, 2, 3, 4, 2, 5, 6, 3, 7, 8, 2]
print_duplicates(my_list)